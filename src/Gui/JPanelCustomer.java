    private int getStateID() throws SQLException{
        ResultSet rs = statement.executeQuery("Select sid from State S "
                    + "where S.name = '" + this.jComboBoxState.getSelectedItem() + "'");
        rs.next();
        return rs.getInt(1);
    }
    
    private ResultSet getAddress(int sid) throws SQLException{
        return statement.executeQuery("Select aid from Address A "
                    + "Where "
                    + "A.address = '" + this.jTextFiledAddress.getText() + "' and "
                    + "A.city = '" + this.jTextFieldCity.getText() + "' and "
                    + "A.state_id = '" + sid  + "' and "
                    + "A.zipcode = '" + this.jTextFieldZipCode.getText() + "'");
    }
    private int makeAID(int sid) throws SQLException{
        ResultSet rs = getAddress(sid);
        if (rs.next()){
            return rs.getInt(1);
        }
        statement.executeUpdate("insert into Address (address, city, state_id, zipcode) values "
                    + "('" 
                    + this.jTextFiledAddress.getText() + "', '"
                    + this.jTextFieldCity.getText() + "', '"
                    + sid + "', '"
                    + this.jTextFieldZipCode.getText() + "')");
        rs = getAddress(sid);
        rs.next();
        return rs.getInt(1);
    }
    
    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        // TODO add your handling code here:
        try {
            int sid = getStateID();
            int aid = makeAID(sid);
            statement.executeUpdate("insert into Customer(name, email, phone, address_id) values ('"
                + this.jTextFieldName.getText() + "', '"
                + this.jTextFieldEmail.getText() + "', '"
                + this.jTextFieldPhone.getText() + "', '"
                + aid + "')");
            JOptionPane.showMessageDialog(null, "Add Customer Succeded");
            reset();
        } catch (SQLException e) {
            System.out.println("Error " + e.getErrorCode());
            JOptionPane.showMessageDialog(null, "Add Customer Failed");
        }
    }//GEN-LAST:event_jButtonAddActionPerformed

    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        // TODO add your handling code here:
        if(this.jTableCustomer.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Choose a customer to modify");
        } else {
            try {
                statement.executeUpdate("update Customer set "
                        + "name = '" + this.jTextFieldName.getText() + "', "
                        + "email = '" + this.jTextFieldEmail.getText() + "', "
                        + "phone = '" + this.jTextFieldPhone.getText() + "' "
                        + "where cid = " + selectedId);
                ResultSet rs = statement.executeQuery("select address_id from Customer C "
                        + "where C.cid = " + selectedId);
                rs.next();
                int aid = rs.getInt(1);
                statement.executeUpdate("update Address set "
                        + "address = '" + this.jTextFiledAddress.getText() + "', "
                        + "city = '" + this.jTextFieldCity.getText() + "', "
                        + "state_id = '" + getStateID() + "', "
                        + "zipcode = '" + this.jTextFieldZipCode.getText() + "' "
                        + "where aid = " + aid);
                JOptionPane.showMessageDialog(null, "Update Success");
                reset();
            } catch (SQLException e) {
                System.out.println(e.getErrorCode());
                JOptionPane.showMessageDialog(null, "Update Failed");
            }
            
        }
    }//GEN-LAST:event_jButtonUpdateActionPerformed

    private void jButtonDeteleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeteleActionPerformed
        if (this.jTableCustomer.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Choose a customer to delete");
        } else {
            try {
                ResultSet rs = statement.executeQuery("select address_id from Customer C "
                        + "where C.cid = " + selectedId);
                rs.next();
                int aid = rs.getInt(1);
                statement.executeUpdate("delete from Customer where cid = " + selectedId);
                
                rs = statement.executeQuery("select cid from Customer C "
                        + "where C.address_id = " + aid);
                if (!rs.next()) {
                    statement.executeUpdate("delete from Address where aid = " + aid);
                }
                JOptionPane.showMessageDialog(null, "Delete Succeded");
                reset();
            } catch (SQLException e) {
                System.out.println(e.getErrorCode());
                JOptionPane.showMessageDialog(null, "Delete Failed");
            }
        }
    }//GEN-LAST:event_jButtonDeteleActionPerformed

