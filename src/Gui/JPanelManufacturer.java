    // Load Headers and row for Manufacturer Table
    private void loadTable(){
        // Headers
        String headers[] = {"Id", "Manufacturer Name"};
        
        // Set # of row to 0
        dtm.setRowCount(0);
        dtm.setColumnIdentifiers(headers);
        this.jTableManufacturer.setModel(dtm);
        try {
            ResultSet rs = statement.executeQuery("Select * from Manufacturer");
            while (rs.next()) {
                dtm.addRow(new Object[]{rs.getInt(1), rs.getString(2)});
            }
        } catch (SQLException e) {
            
        } 
    }
    
    // Reset the textfiled and load table
    private void reset(){
        this.jTextFieldId.setText("");
        this.jTextFieldManufacturerName.setText("");
        loadTable();
    }

