    // Update Manufacturer
    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        // TODO add your handling code here:
        if(this.jTableManufacturer.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Choose a manufacturer to moldify");
        } else {
            try {
                statement.executeUpdate("update Manufacturer set name = '" +
                        this.jTextFieldManufacturerName.getText() + "' where mid = " + selectedId);
               
                JOptionPane.showMessageDialog(null, "Update Succeded");
                reset();
            } catch (SQLException e) {
                //String updateString = "upadte Manufacturer set name = ? where mid = ?";
                System.out.println(e.getErrorCode());
                JOptionPane.showMessageDialog(null, "Update Failed");
                
                
            }
        }
    }//GEN-LAST:event_jButtonUpdateActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
         if(this.jTableManufacturer.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Choose a manufacturer to delete");
        } else {
            try {
                
                statement.executeUpdate("delete from Manufacturer where mid = " + selectedId);
               
                JOptionPane.showMessageDialog(null, "Delete Succeded");
                reset();
            } catch (SQLException e) {
                //String updateString = "upadte Manufacturer set name = ? where mid = ?";
                System.out.println(e.getErrorCode());
                JOptionPane.showMessageDialog(null, "Detele Failed");
                
                
            }
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

